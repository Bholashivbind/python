import sys

# Find the size of int
print("Size of int: ", sys.getsizeof(int()))

# Find the size of float
print("Size of float: ", sys.getsizeof(float()))

# Find the size of double (float in python)
print("Size of double: ", sys.getsizeof(float()))

# Find the size of char (string in python)
print("Size of char: ", sys.getsizeof('a'))
